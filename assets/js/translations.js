// Make translations globally available

/**
 * Simple utility function to directly apply translations
 */
function applyPageTranslations(lang) {
    const elements = document.querySelectorAll('[data-translate]');

    elements.forEach(element => {
        const key = element.getAttribute('data-translate');

        if (window.translations[key] && window.translations[key][lang]) {
            if (element.hasAttribute('placeholder')) {
                element.setAttribute('placeholder', window.translations[key][lang]);
            } else {
                element.textContent = window.translations[key][lang];
            }
        }
    });
}

// When document is loaded
document.addEventListener('DOMContentLoaded', function() {
    // Get saved language or default to Polish
    const savedLanguage = localStorage.getItem('selectedLanguage') || 'pl';

    // Apply translations
    applyPageTranslations(savedLanguage);
});